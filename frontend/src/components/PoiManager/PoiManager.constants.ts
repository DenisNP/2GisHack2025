import { Poi, PoiType } from '../../types/Poi';

export const EMPTY_POI: Poi = {
    id: 0,
    point: { x: 0, y: 0 },
    weight: 0,
    type: PoiType.Low,
    geoPoint: { lng: 0, lat: 0 },
};

export const weightByType = (type: PoiType): number => {
    switch (type) {
        case PoiType.Low:
            return 0.2;
        case PoiType.Medium:
            return 0.5;
        case PoiType.High:
            return 1;
    }
};

export const typeByRubric = (rubricId: string): PoiType => {
    switch (rubricId) {
        // High - высокая популярность (посещают очень часто)
        case '14': // Продукты питания / Напитки
        case '6547': // Торговые комплексы / Спецмагазины
        case '5': // Медицина / Здоровье / Красота
        case '2': // Досуг / Развлечения / Общественное питание
        case '1035': // Одежда / Обувь
            return PoiType.High;

        // Medium - средняя популярность (посещают регулярно)
        case '42903': // Автосервис / Автотовары
        case '19532': // Интернет / Связь / Информационные технологии
        case '3': // Компьютеры / Бытовая техника / Офисная техника
        case '42863': // Электроника / Электротехника
        case '8': // Спорт / Отдых / Туризм
        case '11': // Хозтовары / Канцелярия / Упаковка
        case '6': // Образование / Работа / Карьера
        case '746': // Товары для животных / Ветеринария
        case '4': // Культура / Искусство / Религия
        case '42872': // Текстиль / Предметы интерьера
        case '12': // Транспорт / Грузоперевозки
            return PoiType.Medium;

        // Low - низкая популярность (посещают редко или по специальным случаям)
        case '1': // Город / Власть
        case '787': // Мебель / Материалы / Фурнитура
        case '112663': // Места
        case '13': // Металлы / Топливо / Химия
        case '15': // Оборудование / Инструмент
        case '1621': // Охрана / Безопасность
        case '7': // Реклама / Полиграфия / СМИ
        case '42867': // Строительные / отделочные материалы
        case '9': // Строительство / Недвижимость / Ремонт
        case '969': // Юридические / финансовые / бизнес-услуги
        case '5419': // Аварийные / справочные / экстренные службы
        case '10': // Коммунальные / бытовые / ритуальные услуги
            return PoiType.Low;

        // По умолчанию возвращаем Low для неизвестных рубрик
        default:
            return PoiType.Low;
    }
}